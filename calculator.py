a=int(input("enter integer:"))
b=int(input())
operator=input()
if operator=="+":
    print(a+b)
elif operator=="-":
    print(a-b)
elif operator=="*":
    print(a*b)
elif operator=="/":
    print(a/b)
elif operator=="%":
    print(a%b)
elif operator=="//":
    print(a//b)
elif operator=="**":
    print(a**b)
    
    
