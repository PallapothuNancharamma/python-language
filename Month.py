month=int(input("enter month:"))
if month==1 or month==2:
    print("cold")
elif month==3 or  month==4:
    print("spring")
elif month==5 or  month==6:
    print("summer")
elif month==7 or  month==8:
    print("rain")
elif month==9 or  month==10:
    print("autumn")
elif month==11 or  month==12:
    print("winter")
else:
    print("invalid month",month)
